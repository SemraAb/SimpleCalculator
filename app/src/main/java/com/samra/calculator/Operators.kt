package com.samra.calculator

class Operators  {

    fun addtion( num1: Double ,  num2:Double ):Double{
        var result  = num1+num2
        return result
}
    fun minus( num1: Double ,  num2:Double ):Double{
        var result  = num1-num2
        return result
    }
    fun multiplication( num1: Double ,  num2:Double ):Double{
        var result  = num1*num2
        return result
    }
    fun division( num1: Double ,  num2:Double ):Double{
        var result  = num1/num2
        return result
    }
}

